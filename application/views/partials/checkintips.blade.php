<h6>Tips for a great checkin</h6>
<ul class=liDone>
  <li>Your last checkin will appear in this box, so you can compare what you wanted to get done yesterday with what actually got done.</li>
  <li>Make your tasks achievable in a day. "Get Jane to Agree to Wireframes" is better than "Continue Dealing with Management"</li>
  <li>Make your flags of concern (yellow and red) actionable. "Need help with our ORM Library" is better than "ORM Library Sucks"</li>
  <li>Remember to celebrate with green flags!</li>
</ul>
<p>The syntax is markdown. Create flags in the flag section with a R: Y: and G: prefix. R: is for critical things blocking progress. Y: is for things on the horizon that might block progress. G: is for celebration.</p>